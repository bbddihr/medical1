# 돈을 입력받아 
# 5만원권
# 만원권
# 오천원권 
# 천원권으로 
# 교환해서 출력

money = input("교환할 돈을 입력하세요 >>")
money= int(money)
n50000, n10000, n5000, n1000=0,0,0,0

n50000 = money//50000
n10000 = (money%50000)//10000
n5000 = ((money%50000)%10000)//5000
n1000 = (((money%50000)%10000)%5000)//1000

print('입력한 금액: {}'.format(money))
print('50000원권 : {}'.format(n50000))
print('10000원권 : {}'.format(n10000))
print('5000원권 : {}'.format(n5000))
print('1000원권 : {}'.format(n1000))